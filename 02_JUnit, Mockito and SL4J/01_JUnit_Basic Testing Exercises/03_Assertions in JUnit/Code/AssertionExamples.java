package com.example;

public class AssertionExamples {

    public int sum(int a, int b) {
        return a + b;
    }

    public boolean isGreater(int a, int b) {
        return a > b;
    }

    public boolean isLesser(int a, int b) {
        return a < b;
    }

    public String getNullString() {
        return null;
    }

    public String getNonNullString() {
        return "JUnit";
    }
}
